package com.farmateste.farmateste.drug;

/**
 * Enum que representa diferentes tipos de medicamentos.
 */
public enum DrugType {
    ANALGESIC,      // Medicamentos para alívio da dor
    ANTIBIOTIC,     // Medicamentos usados para tratar infecções bacterianas
    ANTIHISTAMINE,  // Medicamentos usados para aliviar os sintomas de alergia
    VACCINE         // Preparações usadas para fornecer imunidade a doenças
}